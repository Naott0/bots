# формула расстояния между двумя точками: ((x2-x1)**2 + (y2-y1)**2)**0.5

route = [(1, 0), (2, 5), (4, 7), (3, 9)]

formula = [(((route[i][0] - route[i + 1][0]) ** 2 + (route[i][1] - route[i + 1][1]) ** 2) ** 0.5) for i in
           range(len(route) - 1)]
print(formula)
